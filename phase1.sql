CREATE DATABASE health_tracker;
USE health_tracker;

CREATE TABLE Users
(
    UserID INT PRIMARY KEY AUTO_INCREMENT,
    Fname VARCHAR (50),
    Lname VARCHAR (50),
    Email VARCHAR (100),
    Birthdate DATE,
    Height DECIMAL(5, 2),
    Weight DECIMAL (5, 2),
    Privilege CHAR (1)  -- either 'a' or 'u'
);

CREATE TABLE Meals (
    FoodID INT PRIMARY KEY AUTO_INCREMENT,
    UserID INT,
    Name VARCHAR(50),
    Timestamp DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    Calories INT,
    Notes TEXT,
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);

CREATE TABLE Exercises (
    ExerciseID INT PRIMARY KEY AUTO_INCREMENT,
    UserID INT,
    Name VARCHAR(50),
    Timestamp DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    Calories_Burned INT,
    Notes TEXT,
    Duration TIME,
    Effort_Rating INT,
    Done BOOL,
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);

CREATE TABLE Nutrients (
    Name VARCHAR(50),
    FoodID INT,
    Amount INT, -- measured in mg
    PRIMARY KEY (Name, FoodID),
    FOREIGN KEY (FoodID) REFERENCES Meals(FoodID)
);

CREATE TABLE Goals
(
    Name VARCHAR (50) PRIMARY KEY,
    UserID INT,
    Target VARCHAR (255),
    Duration TIME,
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);

